package cn.itcast.crawler.autohome.pojo;

/**
 * @author Administrator
 */
public class Edu {
    private Integer edu_id;
    private String edu_name;
    private String edu_start_url;
    private String edu_end_url;
    private String edu_information_url;

    public Integer getEdu_id() {
        return edu_id;
    }

    public void setEdu_id(Integer edu_id) {
        this.edu_id = edu_id;
    }

    public String getEdu_name() {
        return edu_name;
    }

    public void setEdu_name(String edu_name) {
        this.edu_name = edu_name;
    }

    public String getEdu_start_url() {
        return edu_start_url;
    }

    public void setEdu_start_url(String edu_start_url) {
        this.edu_start_url = edu_start_url;
    }

    public String getEdu_end_url() {
        return edu_end_url;
    }

    public void setEdu_end_url(String edu_end_url) {
        this.edu_end_url = edu_end_url;
    }

    public String getEdu_information_url() {
        return edu_information_url;
    }

    public void setEdu_information_url(String edu_information_url) {
        this.edu_information_url = edu_information_url;
    }

}
